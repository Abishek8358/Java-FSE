import java.util.Scanner;

public class DatatypeDemonstration {
    public static void main(String[] args) {
        int a=21;
        float b=6.2f;
        double c= 45000.5;
        char d='A';
        boolean e=true;
        System.out.println("Integer Data type: "+a);
        System.out.println("Float Data type : "+b);
        System.out.println("Double Data type : "+c);
        System.out.println("Char Data type : "+d);
        System.out.println("Boolean Data type : "+e);

    }
}
